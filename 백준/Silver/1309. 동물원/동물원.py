N = int(input())

d = [[0, 0, 0] for _ in range(N)]

d[0] = [1, 1, 1]

# 점화식 : D[N][L] = 2*N 칸에 사자를 배치하는 경우의 수, L은 마지막 배치
# D[N][0] = D[N-1][0] + D[N-1][1] + D[N-1][2] -> 배치 안함
# D[N][1] = D[N-1][0] + D[N-1][2] -> 왼쪽 배치
# D[N][2] = D[N-1][0] + D[N-1][1] -> 오른쪽 배치
for i in range(1, N):
    d[i][0] = (d[i - 1][0] + d[i - 1][1] + d[i - 1][2]) % 9901
    d[i][1] = (d[i - 1][0] + d[i - 1][2]) % 9901
    d[i][2] = (d[i - 1][0] + d[i - 1][1]) % 9901

print(sum(d[N - 1]) % 9901)
